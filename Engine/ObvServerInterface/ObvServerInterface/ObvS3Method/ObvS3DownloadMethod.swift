import Foundation
import ObvCrypto


public protocol ObvS3DownloadMethod: ObvS3Method {}

public extension ObvS3DownloadMethod {
    
    func downloadTask(within session: URLSession) throws -> URLSessionDownloadTask {
        let request = try getURLRequest(httpMethod: "GET", dataToSend: nil)
        let task = session.downloadTask(with: request)
        return task
    }
    
}
