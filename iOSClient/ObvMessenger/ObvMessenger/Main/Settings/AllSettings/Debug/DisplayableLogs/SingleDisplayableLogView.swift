import SwiftUI

@available(iOS 13, *)
struct SingleDisplayableLogView: View {
    
    let content: String
    let shareAction: () -> Void
    
    var body: some View {
        ScrollView {
            VStack {
                HStack {
                    Text(content)
                        .font(.system(.footnote, design: .monospaced))
                    Spacer()
                }
                .padding()
                Spacer()
            }
        }
        .navigationBarItems(trailing: Button(action: shareAction, label: {
            Image(systemName: "square.and.arrow.up")
        }))
    }
}


