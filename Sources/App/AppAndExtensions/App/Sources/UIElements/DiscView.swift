import UIKit

final class DiscView: UIView {

    var color = UIColor.blue
    
    override func draw(_ rect: CGRect) {
        self.backgroundColor = .clear
        let path = UIBezierPath(ovalIn: self.bounds)
        color.setFill()
        path.fill()
    }

}
